def twoCrystalBalls(array) -> int:
    jumpNumber = math.floor(math.sqrt(len(array)))
    i = jumpNumber
    for i in range(0, len(array), jumpNumber):
        if array[i]:
            break
    
    i -= jumpNumber
    for i in range(i, len(array), 1):
        if array[i]:
            return i
    
    return -1

